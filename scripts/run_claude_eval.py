"""
Re-evaluate generation_results_testing.json using Claude with the updated
Shakespearean-context prompt, and produce output.json-format aggregated results.

Usage:
    ANTHROPIC_API_KEY=sk-... python scripts/run_claude_eval.py
"""

import json
import os
import re
import time
from pathlib import Path

import anthropic
import numpy as np

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
BASE = Path(__file__).parent.parent / "latest_results" / "posterior-prior"
GENERATION_RESULTS_PATH = BASE / "generation_results_testing.json"
OUTPUT_PATH = BASE / "output_claude_updated.json"
RAW_SCORES_PATH = BASE / "raw_scores_claude_updated.json"

# ---------------------------------------------------------------------------
# Updated evaluation prompt (mirrors prepare_judge_prompt in llm_pipeline.py)
# ---------------------------------------------------------------------------

EVAL_CRITERIA = """\
IMPORTANT CONTEXT: These texts were generated by a small language model trained EXCLUSIVELY on Shakespearean works (plays and sonnets). The model has no knowledge of modern English conventions, contemporary topics, or any text outside Shakespeare's corpus. ALL outputs will be in Early Modern English Shakespearean style — this is a hard constraint of the model, not a stylistic choice. Evaluate accordingly:
- Do NOT penalise archaic vocabulary, iambic phrasing, or Shakespearean dramatic conventions.
- Do NOT expect modern prose, even for modern-sounding prompts.
- The prompts span several categories with different expectations:
  * In-domain Shakespeare (e.g. famous lines, character dialogue): high quality and relevance are achievable — score strictly.
  * Stylistic/creative (emotional states, philosophical themes): quality and diversity matter most; relevance is secondary.
  * Out-of-domain generalization (e.g. "Once upon a time", modern-ish themes): the model will respond in Shakespearean style regardless — judge relevance on whether the model captures the thematic or narrative intent of the prompt in its own idiom, not on whether it sounds modern.
  * Ambiguous/uncertainty prompts (vague or multi-continuation prompts): lower relevance is structurally expected; weight quality and diversity more heavily.
  * Stress tests (very short prompts, mid-sentence starts): be lenient — these are edge cases designed to test limits.

For each text, evaluate on these criteria:

1. **Quality (0-10)**: Assess coherence and stylistic consistency within a Shakespearean register
   - 0-3: Incoherent or grammatically broken even by Early Modern English standards
   - 4-6: Partially coherent; some plausible Shakespearean phrasing but fragmented
   - 7-8: Largely coherent; reads like plausible Shakespeare-era prose or verse
   - 9-10: Highly coherent; convincingly Shakespearean in structure and style

2. **Diversity (0-10)**: Assess variety and creativity relative to the Shakespearean corpus
   - 0-3: Repetitive or near-verbatim reproduction of training data phrases
   - 4-6: Some variation but heavily templated or formulaic
   - 7-8: Meaningfully varied; explores different themes, characters, or imagery within the style
   - 9-10: Highly inventive within the Shakespearean idiom; novel combinations of themes or language

3. **Relevance (0-10)**: Assess how well the generated text continues or responds to the given prompt in a Shakespearean context
   - 0-3: Ignores the prompt or veers into complete non-sequiturs
   - 4-6: Loosely related; picks up some keywords but loses the thread
   - 7-8: Stays on topic; a plausible Shakespearean continuation of the prompt
   - 9-10: Closely follows the prompt's theme, character, or situation in authentic Shakespearean style

Provide your evaluation in the following JSON format:

```json
{
    "unique_id_1": {
        "quality_score": 8.5,
        "diversity_score": 7.0,
        "relevance_score": 9.0,
        "brief_reasoning": "Brief explanation of scores"
    },
    "unique_id_2": {
        ...
    }
}
```

Only output the JSON, nothing else."""


def build_prompt(prompt: str, results: dict) -> str:
    text = (
        f'You are an expert evaluator of text generation quality. '
        f'You will evaluate multiple text continuations that start from the same prompt.\n\n'
        f'Starting Prompt: "{prompt}"\n\n'
        f'Below are {len(results)} different text continuations. Evaluate each one carefully.\n\n'
    )
    for uid, result in results.items():
        if "error" in result:
            continue
        text += f"{'='*80}\nText ID: {uid}\n{'='*80}\n{result['generated_text']}\n\n"
    text += EVAL_CRITERIA
    return text


def extract_json(raw: str) -> dict:
    # Try fenced block first, then bare JSON
    match = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", raw, re.DOTALL)
    if match:
        return json.loads(match.group(1))
    # Find outermost { ... }
    start = raw.find("{")
    end = raw.rfind("}") + 1
    if start != -1 and end > start:
        return json.loads(raw[start:end])
    raise ValueError("No JSON found in response")


def evaluate_all(generation_results: dict, client: anthropic.Anthropic) -> dict:
    # Group by prompt
    by_prompt: dict[str, dict] = {}
    for uid, result in generation_results.items():
        if "error" in result:
            continue
        p = result["prompt"]
        by_prompt.setdefault(p, {})[uid] = result

    all_scores: dict[str, dict] = {}
    total = len(by_prompt)

    for i, (prompt, results) in enumerate(by_prompt.items(), 1):
        print(f"[{i}/{total}] Evaluating prompt: {prompt[:60]}...")
        prompt_text = build_prompt(prompt, results)

        for attempt in range(3):
            try:
                response = client.messages.create(
                    model="claude-sonnet-4-6",
                    max_tokens=4096,
                    messages=[{"role": "user", "content": prompt_text}],
                )
                raw = response.content[0].text
                scores = extract_json(raw)
                all_scores.update(scores)
                print(f"  -> {len(scores)} scored")
                break
            except (json.JSONDecodeError, ValueError) as e:
                print(f"  Parse error (attempt {attempt+1}): {e}")
                if attempt == 2:
                    print("  Skipping this prompt group after 3 failures.")
            except Exception as e:
                print(f"  API error (attempt {attempt+1}): {e}")
                time.sleep(5)

        time.sleep(0.5)  # gentle rate limiting

    return all_scores


def aggregate(generation_results: dict, scores: dict) -> dict:
    model_stats: dict[str, dict] = {}

    for uid, result in generation_results.items():
        if "error" in result or uid not in scores:
            continue

        model_path = result["model_path"]
        s = scores[uid]

        if model_path not in model_stats:
            model_stats[model_path] = {
                "quality_scores": [],
                "diversity_scores": [],
                "relevance_scores": [],
                "by_config": {},
            }

        model_stats[model_path]["quality_scores"].append(s["quality_score"])
        model_stats[model_path]["diversity_scores"].append(s["diversity_score"])
        model_stats[model_path]["relevance_scores"].append(s["relevance_score"])

        config_key = f"temp_{result['temperature']}_topk_{result['top_k']}_samples_{result['num_samples']}"
        cfg = model_stats[model_path]["by_config"].setdefault(
            config_key, {"quality_scores": [], "diversity_scores": [], "relevance_scores": [], "count": 0}
        )
        cfg["quality_scores"].append(s["quality_score"])
        cfg["diversity_scores"].append(s["diversity_score"])
        cfg["relevance_scores"].append(s["relevance_score"])
        cfg["count"] += 1

    final: dict[str, dict] = {}
    for model_path, stats in model_stats.items():
        final[model_path] = {
            "overall_avg_quality": float(np.mean(stats["quality_scores"])),
            "overall_avg_diversity": float(np.mean(stats["diversity_scores"])),
            "overall_avg_relevance": float(np.mean(stats["relevance_scores"])),
            "total_generations": len(stats["quality_scores"]),
            "by_config": {
                ck: {
                    "avg_quality": float(np.mean(cv["quality_scores"])),
                    "avg_diversity": float(np.mean(cv["diversity_scores"])),
                    "avg_relevance": float(np.mean(cv["relevance_scores"])),
                    "count": cv["count"],
                }
                for ck, cv in stats["by_config"].items()
            },
        }
    return final


def main():
    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        raise SystemExit("Set ANTHROPIC_API_KEY environment variable first.")

    client = anthropic.Anthropic(api_key=api_key)

    print(f"Loading {GENERATION_RESULTS_PATH} ...")
    with open(GENERATION_RESULTS_PATH) as f:
        generation_results = json.load(f)
    print(f"  {len(generation_results)} entries loaded.")

    raw_scores = evaluate_all(generation_results, client)

    # Save raw per-id scores
    with open(RAW_SCORES_PATH, "w") as f:
        json.dump(raw_scores, f, indent=2)
    print(f"\nRaw scores saved to {RAW_SCORES_PATH}")

    # Aggregate into output.json format
    aggregated = aggregate(generation_results, raw_scores)

    with open(OUTPUT_PATH, "w") as f:
        json.dump(aggregated, f, indent=2)
    print(f"Aggregated results saved to {OUTPUT_PATH}")

    # Quick summary
    print("\n=== Summary ===")
    for model_path, stats in aggregated.items():
        name = model_path.split("/")[-2] if "/" in model_path else model_path
        print(
            f"{name}: Q={stats['overall_avg_quality']:.2f}  "
            f"D={stats['overall_avg_diversity']:.2f}  "
            f"R={stats['overall_avg_relevance']:.2f}  "
            f"(n={stats['total_generations']})"
        )


if __name__ == "__main__":
    main()
